public class Number {
    public static void main(String[] args) {
        byte mybyte = 12;
        short myshort = 32564;
        int myint = 45784612;
        long mylong = 46789451;
        float myfloat = 12.3f;
        double mydouble = 199.00321d;
        double result = mybyte + myshort + myint + mylong+myfloat+mydouble;
        System.out.println(result);
    }
}
